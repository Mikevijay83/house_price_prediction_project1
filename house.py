df.head()

list1 = ['mainroad','guestroom','basement','hotwaterheating','airconditioning','prefarea']

# Fix the typo from df[list] to df[list1]
df[list1] = df[list1].replace({'yes':1, 'no':0})

df.head()

df['furnishingstatus']. nunique()

df['furnishingstatus'] = df['furnishingstatus']. replace({'furnished':1,'semi-furnished':2,'unfurnished':3})

df.head()

from sklearn.preprocessing import StandardScaler

scalar = StandardScaler()

list2 = ['price','area']

df[list2] = scalar.fit_transform(df[list2])

df.head()

df.dtypes

core = df.corr()

try:
    import matplotlib.pyplot as plt
    import seaborn as sns
    plt.figure(figsize=(10,10))
    sns.heatmap(core,annot=True,cmap='coolwarm')
    plt.show()
    df.hist(figsize=(10,10),bins=10)
    plt.show()
except ImportError:
    print("Visualization libraries (matplotlib/seaborn) not installed. Skipping plotting.")


x = df.drop('price',axis = 1)
y = df['price']

x

y

x
y

from sklearn.model_selection import train_test_split

df.head()

x_train,x_test,y_train,y_test = train_test_split(x,y,test_size = 0.2, random_state=4)

from sklearn.linear_model import LinearRegression

lr = LinearRegression()

lr.fit(x_train, y_train)

y_predict = lr.predict (x_test)

from sklearn.metrics import r2_score

lr_accuracy = r2_score(y_test, y_predict)

print("Linear Regression R2 Accuracy Score:", lr_accuracy)
